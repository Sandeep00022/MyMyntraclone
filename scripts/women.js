let container1=document.getElementById("container1")

  const AppendMensCloths=(arr)=>{

    container1.innerText=""

    arr.forEach((el)=>{

        let div=document.createElement("div")
        div.className="container"

        let btn =document.createElement("div")
        btn.innerText="Add to Cart"
        btn.className="overlay"
        btn.addEventListener("click",()=>{
            AddtoCart(el)
        })
        
        let img=document.createElement("img")
        img.src=el.image
        img.className="image"

        img.style.width="100%";


        let brand=document.createElement("h4")
        brand.innerText=el.name

        let det=document.createElement("p")
        det.innerText=el.detail

        let div2=document.createElement("div")
        
        div2.id="div2"
        

        let bhau=document.createElement("h4")
            bhau.innerText="₹"+el.price;

            let bhau2=document.createElement("p")
            bhau2.innerText="₹"+el.strickedoff;
            bhau2.style.textDecoration="line-through"
            bhau2.id="bhau2"
            


            let div3 =document.createElement("div")
            div.append(img,btn,brand,det)
            div2.append(bhau2,bhau)
            div3.append(div,div2)

            container1.append(div3)

    })

  }


//   sort function

function Sort(){

  
    let select=document.getElementById("pricesort").value;

    if(select=="high"){
        arr.sort(function(a,b){
            return Number(b.price)-Number(a.price)
        })
    }
    if(select=="low"){
    arr.sort(function(a,b){
            return Number(a.price)-Number(b.price)
        })

    }

    AppendMensCloths(arr)
  }

  AppendMensCloths(arr)


//   add to cart function
  const AddtoCart=(el)=>{

    alert("Added to your bag")
    let array=JSON.parse(localStorage.getItem("cart"))||[]
    array.push(el)
    localStorage.setItem("cart",JSON.stringify(array))
  }