function Sort(){

  
    let select=document.getElementById("pricesort").value;

    if(select=="high"){
        arr.sort(function(a,b){
            return Number(b.price)-Number(a.price)
        })
    }
    if(select=="low"){
    arr.sort(function(a,b){
            return Number(a.price)-Number(b.price)
        })

    }

    AppendMensCloths(arr)
  }

